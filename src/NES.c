void	NES_SetCPUMode (int NewMode)
{
	HMENU hMenu = GetMenu(mWnd);
	if (NewMode == 0)
	{
		CheckMenuRadioItem(hMenu,ID_PPU_MODE_NTSC,ID_PPU_MODE_PAL,ID_PPU_MODE_NTSC,MF_BYCOMMAND);
		PPU.SLEndFrame = 262;
		if (PPU.SLnum >= PPU.SLEndFrame)	/* if we switched from PAL, scanline number could be invalid */
			PPU.SLnum = PPU.SLEndFrame - 1;
		GFX.WantFPS = 60;
		GFX_LoadPalette(0);
		APU_SetFPS(60);
		PPU.IsPAL = FALSE;
	}
	else
	{
		CheckMenuRadioItem(hMenu,ID_PPU_MODE_NTSC,ID_PPU_MODE_PAL,ID_PPU_MODE_PAL,MF_BYCOMMAND);
		PPU.SLEndFrame = 312;
		GFX.WantFPS = 50;
		GFX_LoadPalette(1);
		APU_SetFPS(50);
		PPU.IsPAL = TRUE;
	}
}

